    def send_movement(self, positions, seconds):
        msg = JointTrajectory()
        msg.joint_names = self.joint_names
        point = JointTrajectoryPoint()
        point.positions = positions
        
        # Convierte segundos (ej. 1.5) a segundos enteros y nanosegundos
        point.time_from_start = Duration(sec=int(seconds), nanosec=int((seconds % 1) * 1e9))
        msg.points.append(point)
        self.publisher.publish(msg)

    def from_gui(self, poses_gui, default_t=1.0):
        """
        Convierte poses capturadas desde el GUI al sistema de radianes.
        Acepta una lista de listas (solo posiciones) o una lista de tuplas (posiciones, tiempo).
        """
        K = math.pi / 5
        OFFSET_CODO = math.pi - 1.75  # ≈ 1.3916

        result = []
        for item in poses_gui:
            # Revisar si el item es una tupla con (posiciones, tiempo_personalizado)
            if isinstance(item, tuple) and len(item) == 2:
                pos, current_t = item
            else:
                pos = item
                current_t = default_t
                
            converted = []
            for idx, val in enumerate(pos):
                if idx == 7 or idx == 11:  # joint_8 y joint_12
                    converted.append(round(val * K + OFFSET_CODO, 4))
                else:
                    converted.append(round(val * K, 4))
            result.append((converted, current_t))
        return result

